class Flashcard {
  String name, face, back;

  Flashcard(String inputName, String inputFace, String inputBack) {
    this.name = inputName;
    this.face = inputFace;
    this.back = inputBack;
  }

  String getFace() {
    return this.face;
  }

  String getBack() {
    return this.back;
  }

  String getName() {
    return this.back;
  }
}
